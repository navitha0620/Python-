#range clss-->It is used to generate sequence of numbers
#It is also a collection of object
r1=range(10)#0 to 9 it will print
print("Elements in the range objects\t\n")
for i in r1:
    print(i,end=",")
    
r2=range(1,11)
print("Elements in the range objects\t\n")
for i in r2:
    print(i,end=",")
    
r3=range(1,11,2)#Step value
print("Elements in the range objects\t\n")
for i in r3:
    print(i,end=",")
