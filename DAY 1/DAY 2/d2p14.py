#SETS
#SET CAN STORE SAME OR DIFFERENT TYPES OF ELEMENTS IN AN SET
#WE CAN'T STORE DUPLICATE ELEMENTS
#IT DOESN'T MAINTAIN ANY ORDER
#SET IS AN MUTABLE OBJECT
# WE CAN CREATE A SET BY USING {} CURLE BRACES OR set()
s1={} #Dictionary Element is created without any elements
s2=set()
print(s1)
print(s2)
print(type(s1))
print(type(s2))

#IT IS AN SET 
s1={10,20}
print(s1)
print(type(s1))
