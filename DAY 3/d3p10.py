#bin() METHOD-->IT CONVERTS INTO BINARY
x=10
print(f"x:\t{x}")
print(f"bin(x):\t={bin(x)}")
#OCTAL TO DECIMAL & BINARY
y=0o12
print(f"y:\t{y}")
print(f"bin(y):\t={bin(y)}")
#HEXADECIMAL TO DECIMAL & BINARY 
z=0x15
print(f"z:\t{z}")
print(f"bin(z):\t={bin(z)}")
#BINARY TO DECIMAL & OCTAL , HEXADECIMAL
b=0b11111111
print(f"b:\t{b}")
print(f"oct(b):\t={oct(b)}")
print(f"hex(b):\t={hex(b)}")


